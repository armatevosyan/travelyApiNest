export default class PlaceOptionSeeder implements Seeder {
  async run(dataSource: DataSource): Promise<void> {
    const placeOptionRepository = dataSource.getRepository(PlaceOption);

    console.log('üü¢ Seeding place options...');

    for (const placeOptionData of placeOptionSeeds) {
      const existingPlaceOption = await placeOptionRepository.findOne({
        where: { slug: placeOptionData.slug },
      });

      if (!existingPlaceOption) {
        const placeOption = placeOptionRepository.create(placeOptionData);
        await placeOptionRepository.save(placeOption);
        console.log(
          `‚úÖ Created place option [${placeOptionData.category}]: ${placeOptionData.name}`,
        );
      } else {
        console.log(`‚è≠Ô∏è  Place option already exists: ${placeOptionData.name}`);
      }
    }

    console.log('üéâ Place option seeding completed!');
  }
}
